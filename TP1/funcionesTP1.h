/** \brief Devuelve la suma de los paràmetros
 *
 * \param a float Primer valor
 * \param b float Segundo valor
 * \return float La suma de los parámetros
 *
 */
int sumar(int a, int b);


/** \brief Devuelve la diferencia de los parámetros
 *
 * \param a float Primer valor
 * \param b float Segundo Valor
 * \return float Diferencia de los parámetros
 *
 */
int restar(int a, int b);


/** \brief Devuelve el cociente de los parámetros
 *
 * \param a float Primer valor
 * \param b float Segundo Valor
 * \return float Cociente de los parámetros
 *
 */
float dividir(int a, int b);


/** \brief Devuelve el producto de los parámetros
 *
 * \param a float Primer valor
 * \param b float Segundo Valor
 * \return float Producto de los parámetros
 *
 */
int multiplicar(int a, int b);

/** \brief Calcula el factorial de un numero
 *
 * \param Numero ingresado
 * \param
 * \return Retorna el resultado del factorial del numero ingresado
 *
 */
long long int factorialCalcular (int a);




